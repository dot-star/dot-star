pomodoro() {
    # TODO: Play at different volumes depending on sound output device.
    #   Volume with headphones: 0.25
    #   Volume with laptop speakers: 0.5

    _play_media() {
        local prompt="${1}"

        read < <(ffplay -loop -1 -nodisp -loglevel quiet -af "volume=0.25" <(xxd --revert --plain <(echo "${MEDIA}")) & echo "${!}")
        pid="${REPLY}"

        if [[ -n "${ZSH_VERSION}" ]]; then
            echo -n "${prompt}"
            read REPLY
        elif [[ -n "${BASH_VERSION}" ]]; then
            read -p "${prompt}" -n 1 -s
        fi

        kill "${pid}"
        echo -e -n "\r${ceol}"
        if [[ $REPLY =~ ^[Qq]$ ]]; then
            (exit 1)
        fi
    }

    _countdown_from() {
        starting_sec="${1}"
        hint="${2}"

        for ((i = "${starting_sec}"; i > 0; i--)); do
            echo -e -n "\r${ceol}${i} (${hint})"
            sleep 1
        done
    }

    TASK_SEC="$(echo "60*25" | bc)"
    BREAK_SEC="$(echo "60*5" | bc)"

    while :; do
        ceol="$(tput el)"

        _countdown_from "${TASK_SEC}" "task"
        echo -e -n "\r${ceol}"
        _play_media "Press any key to start your break or press [q] to quit: "
        if [ $? -ne 0 ]; then
            break
        fi

        _countdown_from "${BREAK_SEC}" "break"
        echo -e -n "\r${ceol}"
        _play_media "Resume your task by pressing any key or press [q] to quit: "
        if [ $? -ne 0 ]; then
            break
        fi
    done
}

